public class JFT6Ex11
{
	public static void main (String args [])
	{
		int noOfFishInTank = 6;
		
		switch (noOfFishInTank){
		case 0:
			System.out.println("No fish in tank");
		break;
			
		case 1:
			System.out.println("1 fish in tank");
		break;
		
		case 2:
			System.out.println("2 fish in tank");
		break;
		
		case 3:
			System.out.println("3 fish in tank");
		break;
		
		default:
			System.out.println("Many fish in tank");
		
		}
	}
}